class User {
  constructor(
    telefone_number,
    userName,
    surname,
    nickName,
    country,
    email,
    ID,
    password
  ) {
    this.telefone_number = telefone_number;
    this.userName = userName;
    this.surname = surname;
    this.nickName = nickName;
    this.country = country;
    this.email = email;
    this.ID = ID;
    this.password = password;
  }
}

